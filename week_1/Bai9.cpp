#include <iostream>
using namespace std;

int main() {
    cout << "Kiểu char có kích thước: " << sizeof(char) << " byte" << endl;
    cout << "Kiểu int có kích thước: " << sizeof(int) << " byte" << endl;
    cout << "Kiểu float có kích thước: " << sizeof(float) << " byte" << endl;
    cout << "Kiểu double có kích thước: " << sizeof(double) << " byte" << endl;
    return 0;
}